	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=VALUE1
	LDR R1,=VALUE2
	LDR R5,=result
	
	MOV R4,#4
	
	MOV R9,#0x20000000
	MSR XPSR, R9
	
BACK	LDR R7,[R0]
		LDR R8,[R1]
		
		SBCS R3, R8, R7    ;r8-r7      v2-v1
		ADD R0, #0x04
		ADD R1, #0x04
		STR R3, [R5], #4
		SUB R4, #1		
		
		TEQ R4,#0
		BNE BACK
	
	SBC R6,#0
	STR R6, [R5]
STOP B STOP
VALUE1 DCD 0xFFFFFFFF, 0xFFFFFFFF, 0xFFFFFFFF, 0xFFFFFFFF          ;r7
VALUE2 DCD 0x00000000, 0x00000000, 0x00000000, 0x00000000          ;r8
;VALUE1 DCD 0x11111111, 0x11111112, 0x11111113, 0x11111114   ;ROM
;VALUE2 DCD 0x11111111, 0x21111112, 0x31111113, 0x41111114
	AREA mydata, DATA, READWRITE
result DCD 0, 0, 0, 0 ;ram
	END